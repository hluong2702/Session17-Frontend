// Math.abs() 
let absValue1 = Math.abs(-5.5);
console.log(absValue1);  // Output: 5.5
//  Math.abs() trả về giá trị tuyệt đối, tức là loại bỏ dấu âm, kết quả là 5.5.

//  Math.abs()  
let absValue2 = Math.abs(10);
console.log(absValue2);  // Output: 10
//  Math.abs() không thay đổi giá trị nếu số là dương, kết quả là 10.

//  Math.ceil()  
let ceilValue1 = Math.ceil(4.4);
console.log(ceilValue1);  // Output: 5
// Math.ceil() làm tròn số lên, vì 4.4 gần nhất với 5 và 5 là số nguyên lớn hơn 4.4.

//  Math.ceil()  
let ceilValue2 = Math.ceil(5.8);
console.log(ceilValue2);  // Output: 6
//  Math.ceil() làm tròn số lên, vì 5.8 gần nhất với 6 và 6 là số nguyên lớn hơn 5.8.

//  Math.floor()  
let floorValue1 = Math.floor(5.7);
console.log(floorValue1);  // Output: 5
//  Math.floor() làm tròn số xuống, vì 5.7 gần nhất với 5 và 5 là số nguyên nhỏ hơn 5.7.

//  Math.floor()  
let floorValue2 = Math.floor(6.1);
console.log(floorValue2);  // Output: 6
//  Math.floor() không thay đổi giá trị nếu số là nguyên, vì 6.1 gần nhất với 6 và 6 là số nguyên nhỏ hơn 6.1.

//  Math.round() 
let roundValue1 = Math.round(10.1);
console.log(roundValue1);  // Output: 10
//  Math.round() làm tròn xuống vì 10.1 gần nhất với 10 (phần thập phân nhỏ hơn 0.5).

//  Math.round()  
let roundValue2 = Math.round(20.6);
console.log(roundValue2);  // Output: 21
//  Math.round() làm tròn lên vì 20.6 gần nhất với 21 (phần thập phân lớn hơn hoặc bằng 0.5).
